// Build-time asset bundler.
//
// Walks pipeline/ and skill/ and emits src/generated/bundled-assets.ts — a
// base64 map of every runtime file the plugin needs on disk. main.js bundles
// this map; on first run (or after a version change) the plugin writes the
// files back out into the installed plugin folder (see src/utils/assets.ts).
// This lets BRAT/store installs — which only deliver main.js/manifest/styles —
// still get the full pipeline + skill.
//
// Excluded: build artifacts, macOS junk, the local .env, and the high-res
// original images (the pipeline references the compressed *-tiny.* variants;
// the originals stay in the repo for archival but are NOT shipped).

import { readdirSync, readFileSync, statSync, mkdirSync, writeFileSync } from 'fs';
import { join, relative, basename, dirname } from 'path';
import { fileURLToPath } from 'url';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const PLUGIN_DIR = join(SCRIPT_DIR, '..');

const SOURCE_DIRS = ['pipeline', 'skill', 'resources'];

// Files/dirs that must never be bundled.
const EXCLUDED_BASENAMES = new Set([
  '.DS_Store',
  '.env',
  // High-res originals — pipeline uses the *-tiny.* variants instead.
  'bg.png',
  'logo.png',
  'logo-horizontal.png',
]);

function shouldSkip(absPath, relPath) {
  if (EXCLUDED_BASENAMES.has(basename(absPath))) return true;
  // Skip the pipeline build/ output directory entirely.
  if (relPath === 'pipeline/build' || relPath.startsWith('pipeline/build/')) return true;
  return false;
}

function walk(absDir, collected) {
  for (const entry of readdirSync(absDir)) {
    const abs = join(absDir, entry);
    const rel = relative(PLUGIN_DIR, abs).split('\\').join('/');
    if (shouldSkip(abs, rel)) continue;
    const st = statSync(abs);
    if (st.isDirectory()) {
      walk(abs, collected);
    } else if (st.isFile()) {
      collected.push(rel);
    }
  }
}

const files = [];
for (const dir of SOURCE_DIRS) {
  const abs = join(PLUGIN_DIR, dir);
  try {
    walk(abs, files);
  } catch (e) {
    console.error(`[bundle-assets] cannot read ${dir}: ${e.message}`);
    process.exit(1);
  }
}
files.sort();

const entries = files
  .map((rel) => {
    const b64 = readFileSync(join(PLUGIN_DIR, rel)).toString('base64');
    return `  ${JSON.stringify(rel)}: ${JSON.stringify(b64)},`;
  })
  .join('\n');

const out =
  `// AUTO-GENERATED by scripts/bundle-assets.mjs — DO NOT EDIT.\n` +
  `// Base64 of pipeline/ + skill/ runtime files, written to disk on first run.\n` +
  `export const BUNDLED_ASSETS: Record<string, string> = {\n${entries}\n};\n`;

const outDir = join(PLUGIN_DIR, 'src', 'generated');
mkdirSync(outDir, { recursive: true });
writeFileSync(join(outDir, 'bundled-assets.ts'), out);

const totalKb = files.reduce((s, rel) => s + statSync(join(PLUGIN_DIR, rel)).size, 0) / 1024;
console.log(`[bundle-assets] bundled ${files.length} files (${totalKb.toFixed(0)} KB raw) -> src/generated/bundled-assets.ts`);
